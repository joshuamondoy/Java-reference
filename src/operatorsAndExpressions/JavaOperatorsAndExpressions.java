package operatorsAndExpressions;

public class JavaOperatorsAndExpressions {

    public static void main(String[] args) {
        int result = 1 + 2; //1 and 2 is operand and +, = is operator and all together forms an expression
        System.out.println(result);
        result = result - 1; //3 - 1 = 2
        System.out.println(result);
        result = result * 10; //2 * 10 = 20
        System.out.println(result);
        result = result / 4; //20 / 4 = 5
        System.out.println(result);
        result = result % 2; //5 / 2 = 2 remainder 1
        System.out.println(result);

        //we can abbreviate the operators by
        //++ add 1
        //-- subtracted by 1
        //*= multiplied by
        ///= divided by
        //-= subtracted by
        //+= add by

    }
}
